Map::~Map() {}

void Map::LoadMap(int arr[20][25]) {
    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            map[row][col] = arr[row][col];
        }
    }
}

void Map::DrawMap() {
    SDL_RenderCopy(Game::renderer, backgroundTexture.get(), NULL, NULL);
//    TextureManager::Draw(backgroundTexture.get(), NULL, NULL);
    int type = 0;
    for (int row = 0; row < 20; row++) {
        for (int col = 0; col < 25; col++) {
            type = map[row][col];

            src.x = TILE_SIZE * (type-1);

            dest.x = col * TILE_SIZE;
            dest.y = row * TILE_SIZE;

            switch (type) {
                case 1:
                    TextureManager::Draw(tileTexture.get(), src, dest);
                    break;
                default:
                    break;
            }

        }
    }
}